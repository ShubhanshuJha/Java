    public static int root(int a, int b) throws Exception {
        for(int i = 1; i <= a / 2; i++) {
            int temp = i;
            for (int j = 1; j < b; j++) {
                temp = temp * i;
            }
            if (temp == a) {
                return i;
            }
        }
        throw (new Exception("No Perfect Root Found!!!"));
    }

