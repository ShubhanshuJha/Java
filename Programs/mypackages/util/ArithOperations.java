    public static int lcm(int a, int b) {
        int res = 1;
        while(a != 0 && b != 0) {
            if ((a & 1) == 0 && (b & 1) == 0) {
                a >>= 1;
                b >>= 1;
                res <<= 1;
            } else {
                boolean found = false;
                for (int i = 3; i <= a && i <= b; i += 2) {
                    if (a % i == 0 && b % i == 0) {
                        a = a / i;
                        b = b / i;
                        res *= i;
                        found = true;
                        break;
                    }
                }
                if (!found) {
                    res *= (a * b);
                    a = 0;
                    b = 0;
                }
            }
        }
        return res;
    }

